<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use Spatie\Permission\Models\Permission;

class PermissionController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $permission = Permission::all();
        return view('admin.pages.permissions.index', compact('permission'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('admin.pages.permissions.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validate = $request->validate(['name' => 'required', 'min:3']);
        Permission::create($validate);
        activity()->log(auth()->user()->first_name .' '. auth()->user()->last_name . ' has created Permission ' . $request->input('name'));
        return redirect('admin/permissions',)->with('success','New permission created');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(Permission $permission)
    {
        return view('admin.pages.permissions.edit', compact('permission'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Permission $permission)
    {
        $validate = $request->validate(['name' => 'required']);
        $permission->update($validate);

        activity()->log(auth()->user()->first_name . ' ' . auth()->user()->last_name . ' updated ' . $request->input('name') . ' permission');
        return redirect('admin/permissions')->with('success', 'Permission has been updated successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Permission $permission)
    {
        $permission->delete();
        activity()->log(auth()->user()->first_name . ' ' . auth()->user()->last_name .' has deleted ' . $permission->name . ' permission');
        return redirect('admin/permissions')->with('success', 'Permission has been deleted successfully');
    }
}
